public class MathExample {//math类的部分操作
    public static void main(String[] args) {
        // 计算平方根
        double squareRoot = Math.sqrt(16.0);
        System.out.println("Square root of 16.0 is: " + squareRoot);

        // 计算指数
        double exp = Math.exp(1.0);
        System.out.println("Exponent of 1.0 is: " + exp);

        // 计算最大值
        double max = Math.max(1.2, 3.6);
        System.out.println("Max of 1.2 and 3.6 is: " + max);

        // 计算圆周率
        double pi = Math.PI;
        System.out.println("Value of Pi is: " + pi);

        // 计算幂
        double power = Math.pow(2.0, 3.0);
        System.out.println("2.0 raised to the power of 3.0 is: " + power);
    }
}

